package String;

public class NumberOperations {
  public static void main(String[] args) {
    // 2
    System.out.println(1 + 1);
    // 3
    System.out.println(5 - 2);
    // 12
    System.out.println(3 * 4);
    // 3
    System.out.println(6 / 2);
    // 2
    System.out.println(5 / 2);
    // 2.5
    System.out.println(5. / 2);
    // 2.5
    System.out.println(5 / 2.);
    // 2.5
    System.out.println(5. / 2.);
    // 1
    System.out.println(5 % 2);

  }
}
