package String;

public class Floats {
  public static void main(String [] args) {
    // prints 3.1415
    System.out.println(3.1415);
    // prints -1.5
    System.out.println(-1.5);
    // prints 0.0
    System.out.println(0.0);
    // prints 0.5
    System.out.println(.5);
    // prints 5.0
    System.out.println(5.);


  }
}
