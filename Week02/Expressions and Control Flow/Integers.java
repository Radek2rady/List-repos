package String;

public class Integers {
  public static void main(String[] args) {
    // prints int
    System.out.println(42);
    // prints negative int
    System.out.println(-1);
    // prints 0
    System.out.println(0);
    // prints maximum int
    System.out.println(2147483647);
    // prints minimum int
    System.out.println(-2147483648);
    // error. too high
    System.out.println(2147483648);


  }
}
